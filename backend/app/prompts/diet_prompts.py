CATEGORY_PROMPT_MAP = {
    '식단 플랜이 궁금해요': {
        '왜 이 식단 플랜이 추천됐나요?':   WHY_RECOMMENDED_PROMPT,
        '다른 식단 플랜은 어떤 게 있나요?': OTHER_PLANS_PROMPT,
        '이 식단 플랜이 제 건강에 맞나요?': IS_RIGHT_PLAN_PROMPT,
        '식단 플랜을 바꿀 수 있나요?':      CHANGE_PLAN_PROMPT,
    },
    '영양소 계산이 궁금해요': {
        '권장 칼로리는 어떻게 계산됐나요?':      CALORIE_CALC_PROMPT,
        '단백질을 더 먹어도 되나요?':            PROTEIN_MORE_PROMPT,
        '탄수화물을 줄이면 어떤 효과가 있나요?': CARB_REDUCE_PROMPT,
        '지방은 어떤 종류를 먹어야 하나요?':     FAT_TYPE_PROMPT,
    },
    '실천 방법이 궁금해요': {
        '제한 식품을 꼭 지켜야 하나요?': RESTRICT_FOOD_PROMPT,
        '외식 메뉴가 궁금해요':          EATING_OUT_RECOMMEND_PROMPT,
        '이 음식 먹어도 되나요?':        EATING_OUT_OK_PROMPT,
    },
}

GROUP_RULE_PROMPT = """
[그룹 결정 규칙]
- 수축기혈압 120 미만, 이완기혈압 80 미만 → 정상
- 수축기혈압 120 이상 또는 이완기혈압 80 이상 → 고혈압 위험
- 공복혈당 100 미만 → 정상
- 공복혈당 100 이상 → 혈당이상
- BMI 23 미만 → 정상
- BMI 23 이상 → 비만 위험

[식단 플랜 매핑]
- 정상군 → 균형 식단
- 고혈압군 → 저염 식단
- 혈당이상군 → 저탄수화물 식단
- 비만군 → 저칼로리 식단
- 고혈압+혈당이상군 → 저탄수화물·저염 식단
- 고혈압+비만군 → 저칼로리·저염 식단
- 혈당이상+비만군 → 저탄수화물·저칼로리 식단
- 복합위험군 → 치료 식단
"""


def get_diet_prompt(category: str, message: str, context_data: str) -> str:
    category_map = CATEGORY_PROMPT_MAP.get(category, {})
    prompt_template = category_map.get(message, list(category_map.values())[0] if category_map else BASE_PROMPT)
    return prompt_template.format(context_data=context_data) + GROUP_RULE_PROMPT